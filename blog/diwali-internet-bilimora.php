                <h3>UPI payment must not time out</h3>
                <p>Diwali sale checkouts use UPI — BHIM, GPay, PhonePe, Paytm. A UPI payment requires a round-trip connection to the UPI server within 30 seconds. On an unstable or slow connection, the UPI request times out, the order is not placed, and you have to start again — by which time the product is sold out.</p>

                <p>On Nisan's FTTH fiber, UPI transactions complete in 1–2 seconds with near-zero risk of timeout.</p>

