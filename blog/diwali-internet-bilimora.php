                <h2>Why Fast Internet Matters for Diwali Sales</h2>

                <h3>Flash sales require low latency</h3>
                <p>Flipkart and Amazon flash deals go live at midnight or at scheduled times (10 AM, 12 PM). Thousands of people click "Buy Now" simultaneously. The shoppers who succeed are those with the fastest, most stable connections — their request reaches the server first and completes the booking.</p>

