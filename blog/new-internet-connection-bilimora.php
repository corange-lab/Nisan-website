                <h2>Step 5: The Installation Day</h2>
                <p>Once your booking is confirmed, a Nisan technician will visit your home — typically on the same day for Bilimora West customers. Here is what happens during the visit:</p>

                <h3>Fiber Cable Routing</h3>
                <p>The technician will run a thin fiber optic cable from the nearest distribution point on your street into your home. The cable is about 3mm thick and can be routed along walls or through existing cable channels. The technician will discuss the entry point with you before drilling anything.</p>

                <h3>ONU Device Installation</h3>
                <p>An ONU (Optical Network Unit) — sometimes called an ONT — is a small white box, roughly the size of a thick paperback book, that converts the fiber signal into an internet signal your router can understand. Think of it as the bridge between the fiber cable and your home network. The technician will mount it on a wall near your main living area or wherever you want the router placed.</p>

                <h3>Router Setup and WiFi</h3>
                <p>Nisan provides a free WiFi router with every connection. The technician will connect it to the ONU, power it on, and set up your WiFi network. You will choose your own WiFi name (SSID) and password on the spot. The technician will then test the connection by running a speed test to confirm you are receiving the full speed of your plan.</p>

                <h3>Connecting Your Devices</h3>
                <p>Once the WiFi is live, connecting your phone, laptop, smart TV, or any other device is as simple as selecting your WiFi name from the list and entering your password. The technician will stay until all your devices are connected and working.</p>

