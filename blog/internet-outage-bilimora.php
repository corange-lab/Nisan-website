                <h2>Step 1: Check Your Router and ONT Device</h2>

                <p>Look at the indicator lights on your Nisan router and ONT (the white fiber device mounted on your wall):</p>

                <ul>
                    <li><strong>All lights off:</strong> Power issue. Check if the device is plugged in and the socket is working. Try a different socket.</li>
                    <li><strong>ONT PON light blinking red:</strong> The fiber signal is lost. This is a network or cable issue — call Nisan.</li>
                    <li><strong>Router power light on but internet light off/red:</strong> Authentication issue or ISP-side problem. Restart the router first.</li>
                    <li><strong>All lights normal but no internet:</strong> Device-specific issue. Try another device on the same WiFi.</li>
                </ul>

                <h2>Step 2: Restart Your Router (Most Common Fix)</h2>

                <ol>
                    <li>Switch off the router and ONT device from the power button or socket</li>
                    <li>Wait 30 seconds (do not skip this — the router needs time to fully reset)</li>
                    <li>Switch the ONT back on first, wait 60 seconds for it to fully connect</li>
                    <li>Switch the router on and wait another 60 seconds</li>
                    <li>Try connecting again from your phone or laptop</li>
                </ol>

                <p>A simple restart resolves around 40% of home internet issues. Memory overflow in older routers, temporary authentication glitches, and minor signal hiccups all clear with a restart.</p>

                <h2>Step 3: Check If It Is One Device or All Devices</h2>

                <p>If your phone has no internet but your laptop does (or vice versa), the problem is with the specific device, not your connection. Try:</p>

                <ul>
                    <li>Forget the WiFi network on the problem device and reconnect with the password</li>
                    <li>Toggle airplane mode on and off on a phone</li>
                    <li>Restart the device itself</li>
                    <li>Check if the device is connected to the right WiFi network (2.4 GHz vs 5 GHz)</li>
                </ul>

