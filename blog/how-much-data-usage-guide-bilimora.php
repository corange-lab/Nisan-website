                                <tr><td>Browsing &amp; social media</td><td class="nisan-col">50–150 MB</td></tr>
                                <tr><td>Music streaming (Spotify, etc.)</td><td class="nisan-col">50–150 MB</td></tr>
                                <tr><td>Voice call (WhatsApp)</td><td class="nisan-col">30–60 MB</td></tr>
                                <tr><td>Video call (WhatsApp/Zoom)</td><td class="nisan-col">0.5–1.5 GB</td></tr>
                                <tr><td>YouTube / Netflix (SD)</td><td class="nisan-col">~1 GB</td></tr>
                                <tr><td>YouTube / Netflix (HD)</td><td class="nisan-col">~3 GB</td></tr>
                                <tr><td>YouTube / Netflix (4K)</td><td class="nisan-col">~7 GB</td></tr>
                                <tr><td>Online gaming (play)</td><td class="nisan-col">40–150 MB</td></tr>
