                        <h2>Frequently Asked Questions</h2>
                        <div itemscope itemtype="https://schema.org/FAQPage">
                        <h3>How does WiFi actually work?</h3>
                        <p>Your router receives internet from the ISP through a cable, then converts that data into radio waves and broadcasts them wirelessly. Your phone or laptop has a WiFi chip that receives these radio waves and converts them back into data — and sends data back the same way. It is two-way radio communication over short distances.</p>
                        <h3>What is the difference between 2.4 GHz and 5 GHz WiFi?</h3>
                        <p>2.4 GHz travels farther and passes through walls better but is slower and more prone to interference. 5 GHz is much faster with less interference but has shorter range and is weaker through walls. Use 2.4 GHz for range in far rooms and 5 GHz for speed near the router.</p>
                        <h3>Which is better, 2.4 GHz or 5 GHz?</h3>
                        <p>Neither is universally better — they suit different situations. 5 GHz is better for speed-heavy tasks like streaming and gaming when you are close to the router. 2.4 GHz is better for range and for devices far away or behind thick walls. A dual-band router gives you both.</p>
                        <h3>Should I use one WiFi name or separate names for each band?</h3>
                        <p>Most modern routers use one WiFi name and automatically steer each device to the best band (band steering) — this is simplest. If you want manual control, you can split them into two names, e.g. Home-2.4G and Home-5G, and connect devices to the band you prefer.</p>
                        <h3>What is a WiFi channel and does it matter?</h3>
                        <p>Each band is divided into channels. In crowded areas, neighbouring routers on the same channel interfere and slow each other down. Switching to a less busy channel (or letting the router auto-select) improves speed and stability, especially on the 2.4 GHz band.</p>
                        <h3>What about 6 GHz and WiFi 6E?</h3>
                        <p>6 GHz is a newer band (used by WiFi 6E and WiFi 7) offering even more speed and far less congestion, but very short range and only on the latest devices. For most homes today, a good dual-band 2.4/5 GHz router is more than enough.</p>
