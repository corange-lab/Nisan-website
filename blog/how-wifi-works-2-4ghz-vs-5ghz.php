                        <h2 id="how">1. How WiFi Actually Works</h2>
                        <p>WiFi is simply <strong>two-way radio communication</strong> over short distances. Here's the journey of your data:</p>
                        <ol>
                            <li>Internet arrives at your home through a cable from your ISP (for <a href="/blog/ftth-fiber-internet-bilimora">FTTH fiber</a>, that's a thin glass fiber carrying pulses of light).</li>
                            <li>Your <strong>router</strong> receives this data and converts it into <strong>radio waves</strong>.</li>
                            <li>The router broadcasts those radio waves into the air around it.</li>
                            <li>The <strong>WiFi chip</strong> in your phone, laptop or TV picks up the radio waves and converts them back into data — a web page, a video, a message.</li>
                            <li>When you send something (a message, an upload), your device turns it into radio waves that the router catches and sends back out to the internet.</li>
                        </ol>
                        <p>All of this happens hundreds of times a second, invisibly. WiFi uses specific radio <strong>frequencies</strong> — mainly 2.4 GHz and 5 GHz — that are set aside worldwide for this purpose.</p>

                        <div class="info-box"><strong>Analogy:</strong> Think of the router as a radio station and your devices as radios. 2.4 GHz is like an AM station — it reaches far but the sound quality is lower. 5 GHz is like an FM station — crystal clear, but the signal doesn't travel as far.</div>

                        <h2 id="router">2. What a Router Does (Modem vs Router)</h2>
                        <p>People often mix up these two devices:</p>
                        <ul>
                            <li><strong>Modem / ONT:</strong> the device that connects to your ISP's line and brings the internet <em>into</em> your home. On fiber, this is called an <strong>ONT</strong> (Optical Network Terminal).</li>
                            <li><strong>Router:</strong> takes that single internet connection and shares it with all your devices — over WiFi (wireless) and LAN ports (wired). It also assigns each device a local address and manages traffic.</li>
                        </ul>
                        <p>Many home setups combine both into one box. The router's WiFi radios are what create the two bands we're about to compare.</p>

                        <h2 id="bands">3. WiFi Bands &amp; Channels</h2>
                        <p>A <strong>band</strong> is a range of radio frequencies. WiFi mainly uses two: the <strong>2.4 GHz</strong> band and the <strong>5 GHz</strong> band (plus the newer 6 GHz). Each band is divided into smaller <strong>channels</strong> — like lanes on a highway.</p>
                        <p>If many routers nearby use the same channel, they interfere and slow each other down. That's why, in a crowded apartment, changing the WiFi channel (or letting the router auto-select) can noticeably improve speed — especially on 2.4 GHz, which has fewer non-overlapping channels.</p>

                        <h2 id="24">4. The 2.4 GHz Band — Benefits &amp; Drawbacks</h2>
                        <p>2.4 GHz is the older, longer-range band. Its waves are longer, so they travel farther and bend around obstacles better.</p>
                        <h3>Benefits of 2.4 GHz</h3>
                        <ul>
                            <li><strong>Longer range</strong> — reaches distant rooms and outdoors</li>
                            <li><strong>Better wall penetration</strong> — passes through concrete and brick more easily (useful in solid Indian homes)</li>
                            <li><strong>Widely supported</strong> — every device, including old ones and IoT/smart-home gadgets, supports it</li>
                        </ul>
                        <h3>Drawbacks of 2.4 GHz</h3>
                        <ul>
                            <li><strong>Slower speeds</strong> — lower maximum throughput than 5 GHz</li>
                            <li><strong>More interference</strong> — shared with microwaves, cordless phones, Bluetooth and neighbours' WiFi</li>
                            <li><strong>Crowded</strong> — only a few non-overlapping channels, so it clogs up in dense areas</li>
                        </ul>

                        <h2 id="5">5. The 5 GHz Band — Benefits &amp; Drawbacks</h2>
                        <p>5 GHz is the faster, cleaner band. Its waves are shorter, carrying more data but not travelling as far.</p>
                        <h3>Benefits of 5 GHz</h3>
                        <ul>
                            <li><strong>Much faster speeds</strong> — ideal for HD/4K streaming, gaming and large downloads</li>
                            <li><strong>Less interference</strong> — fewer household devices use it</li>
                            <li><strong>More channels</strong> — many non-overlapping channels, so less congestion</li>
                        </ul>
                        <h3>Drawbacks of 5 GHz</h3>
                        <ul>
                            <li><strong>Shorter range</strong> — signal weakens faster with distance</li>
                            <li><strong>Weaker through walls</strong> — thick concrete walls cut it down more than 2.4 GHz</li>
                            <li><strong>Best when close</strong> — needs a fairly clear path to the router for full benefit</li>
                        </ul>

