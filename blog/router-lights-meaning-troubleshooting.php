                        <h1>Router Lights Meaning + Quick Internet Troubleshooting Guide</h1>

                        <p>Those blinking lights on your router (or fiber ONT) aren't random — each one tells you exactly what's working and what isn't. Learn to read them and you can diagnose most internet problems in seconds, before you even call support. Here's what each light means and how to fix common issues.</p>

                        <div class="info-box"><strong>Golden clue:</strong> On a fiber connection, the <strong>LOS light</strong> is the one to watch. It should be <strong>off</strong>. If it's <strong>red or blinking red</strong>, your fiber signal is lost — that's a line issue, not something a restart will fix.</div>

                        <h2>What Each Router / ONT Light Means</h2>
                        <table>
                            <thead>
                                <tr><th>Light</th><th>Healthy state</th><th>What it means</th></tr>
                            </thead>
                            <tbody>
                                <tr><td><strong>Power</strong></td><td class="ok">Steady</td><td>Device is on and getting electricity</td></tr>
                                <tr><td><strong>PON</strong></td><td class="ok">Steady green</td><td>Fiber link to the ISP is established</td></tr>
                                <tr><td><strong>LOS</strong></td><td class="ok">Off</td><td class="bad">Red = Loss of Signal (fiber broken/outage)</td></tr>
                                <tr><td><strong>Internet / WAN</strong></td><td class="ok">Steady/blinking green</td><td>Connected to the internet</td></tr>
                                <tr><td><strong>WiFi / WLAN</strong></td><td class="ok">Blinking green</td><td>WiFi is on and active</td></tr>
                                <tr><td><strong>LAN / 1-4</strong></td><td class="ok">On when used</td><td>A device is plugged in by cable</td></tr>
                            </tbody>
                        </table>
                        <p>Colours can vary slightly by brand, but this covers most fiber routers used in Bilimora homes.</p>

                        <h2>Reading the Lights — Quick Diagnosis</h2>
                        <ul>
                            <li><strong>All normal but no internet on devices:</strong> likely a WiFi issue — see <a href="/blog/how-to-increase-wifi-speed-bilimora">WiFi speed tips</a> or restart the router.</li>
                            <li><strong>Internet/WAN light off, PON green:</strong> the fiber is fine but the connection isn't authenticating — restart, then contact your ISP.</li>
                            <li><strong>PON off or blinking, LOS red:</strong> fiber signal lost — a cut/unplugged fiber or an area outage. Contact your ISP.</li>
                            <li><strong>Power light off:</strong> check the adapter, socket and switch.</li>
                        </ul>

                        <h2>Quick Troubleshooting Steps (In Order)</h2>
                        <ol>
                            <li><strong>Check the lights</strong> against the table above to see where the break is.</li>
                            <li><strong>Restart the router/ONT</strong> — power off for 30 seconds, back on, wait 1–2 minutes.</li>
                            <li><strong>Check the cables</strong> — make sure the fiber and power cables are firmly seated (don't sharply bend fiber).</li>
                            <li><strong>Check if it's an area outage</strong> — ask a neighbour on the same ISP, or check the provider's status page. Nisan customers can view the <a href="/status.php">live network status</a>.</li>
                            <li><strong>Test on one device</strong> to confirm it's not just one phone or laptop.</li>
                            <li><strong>Still down?</strong> Contact your ISP with what the lights show — it speeds up the fix.</li>
                        </ol>

                        <div class="cost-box"><strong>Don't do this:</strong> Never press the small <strong>reset</strong> pinhole unless told to — it wipes all settings including your WiFi name and password. To simply restart, use the power button or unplug the adapter instead.</div>

                        <h2>Why Local Support Fixes It Faster</h2>
                        <p>When the lights show a real line fault, how fast it's fixed depends on your provider. A national ISP gives a ticket and a 24–48 hour window. A <a href="/blog/local-isp-vs-jio-airtel-bilimora">local provider</a> like Nisan sends a Bilimora technician — usually the same day, 365 days a year. See also <a href="/blog/internet-outage-bilimora">how to handle an internet outage</a>.</p>

                        <div class="verdict-box"><strong>Remember:</strong> Power + PON steady green and LOS off = your line is healthy; any problem is likely WiFi or a device. LOS red or PON off = a line issue that needs your ISP. Reading the lights first saves everyone time.</div>

