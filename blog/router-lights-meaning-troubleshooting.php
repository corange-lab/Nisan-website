                        <h2>Frequently Asked Questions</h2>
                        <div itemscope itemtype="https://schema.org/FAQPage">
                        <h3>What does a red LOS light on my router mean?</h3>
                        <p>A red or blinking LOS (Loss of Signal) light on a fiber ONT means the fiber connection is broken — often a cut or unplugged fiber cable, or an area outage. Restarting won't fix a red LOS; contact your ISP. Nisan customers can call or WhatsApp +91 98251 52400.</p>
                        <h3>What do the lights on a router mean?</h3>
                        <p>Typical lights are: Power (device on), PON (fiber link established), LOS (should be off — red means signal lost), Internet/WAN (connected to internet), WiFi (wireless active), and LAN (a device plugged in via cable). Steady green usually means healthy; red or off on PON/Internet means a problem.</p>
                        <h3>How do I fix my internet when it stops working?</h3>
                        <p>Check the router lights, restart the router (power off 30 seconds), check all cables are firmly connected, and confirm it isn't an area outage. If the LOS light is red or the PON light is off, it's a line issue — contact your ISP.</p>
                        <h3>Should I restart my router to fix problems?</h3>
                        <p>Yes — restarting the router fixes many temporary glitches. Power it off for 30 seconds, then back on and wait 1–2 minutes for it to reconnect. If lights still show a fault after restarting, contact your provider.</p>
