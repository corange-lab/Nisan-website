                        <h2>Frequently Asked Questions</h2>
                        <div itemscope itemtype="https://schema.org/FAQPage">
                        <h3>What should I check before buying an internet connection?</h3>
                        <p>Check the connection technology (prefer FTTH fiber), the real download and upload speed, whether bandwidth is shared or dedicated and its contention ratio, any FUP data cap, latency for calls/gaming, uptime and local support response, the total price with GST, and whether installation and the router are free.</p>
                        <h3>What is pure or dedicated bandwidth?</h3>
                        <p>Pure or dedicated bandwidth means the speed is reserved only for you (a 1:1 contention ratio), so you always get full speed. Most home broadband is shared, with a ratio like 1:8, meaning several users share the line and speeds can dip at peak hours. Homes rarely need pure bandwidth; some businesses do.</p>
                        <h3>What is a contention ratio?</h3>
                        <p>A contention ratio is how many customers share the same bandwidth. 1:1 is dedicated; 1:8 means up to 8 users may share it. A lower ratio means more consistent speed, especially during busy evening hours.</p>
                        <h3>Do I need a static IP address for home internet?</h3>
                        <p>No. Most homes work perfectly on a dynamic IP. You only need a static IP if you host a server, need remote CCTV access, run a VPN into your home, or require a fixed address for work. Search "what is my IP" on Google to see your current public IP.</p>
                        <h3>Which internet technology is best — fiber, DSL or air fiber?</h3>
                        <p>FTTH fiber is best for homes and offices: highest and most stable speeds, low latency, unaffected by rain. DSL (copper) is slower and older. Air fiber (wireless) is convenient but can drop in rain and at peak load. Choose fiber wherever available.</p>
                        <h3>How do I find my internet IP address?</h3>
                        <p>For your public IP, search "what is my IP" on Google or visit whatismyipaddress.com. For a device's local IP, check WiFi settings on a phone, run ipconfig on Windows, or ifconfig/ip a on Mac/Linux. Your router's admin page shows both.</p>
