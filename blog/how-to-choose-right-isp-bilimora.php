                        <h2 id="bandwidth">2. Bandwidth — Pure/Dedicated vs Shared</h2>
                        <p><strong>Bandwidth</strong> is how much data can flow per second — measured in Mbps (megabits per second). But the number alone doesn't tell you how it's delivered. This is where "pure bandwidth" comes in.</p>

                        <h3>Pure / dedicated bandwidth</h3>
                        <p><strong>Pure (or dedicated) bandwidth</strong> means the speed is reserved only for you. A "100 Mbps dedicated" line gives you 100 Mbps at all times, no matter what your neighbours do. This is described as a <strong>1:1 contention ratio</strong>. It's more expensive and mostly used by businesses running servers, heavy uploads, or critical operations.</p>

                        <h3>Shared bandwidth &amp; contention ratio</h3>
                        <p>Most home broadband is <strong>shared</strong>. A single large pipe is split among many customers. The <strong>contention ratio</strong> tells you how many:</p>
                        <ul>
                            <li><strong>1:1</strong> — dedicated, yours alone (business grade)</li>
                            <li><strong>1:8</strong> — up to 8 homes may share the line</li>
                            <li><strong>1:16 / 1:32</strong> — more sharing, more chance of evening slowdowns</li>
                        </ul>
                        <p>A lower contention ratio = more consistent speed, especially at busy 8–11 PM hours. A good home ISP keeps enough capacity so shared plans still deliver full speed almost all the time.</p>

                        <div class="info-box"><strong>Do you need pure bandwidth at home?</strong> Almost never. A well-provisioned shared fiber plan gives you full speed nearly always, for a fraction of the price. Ask for pure/dedicated bandwidth only if you run a business server, heavy CCTV upload, or need guaranteed speed 24×7. For that, ask about our <a href="/blog/internet-for-small-business-bilimora">business plans</a>.</div>

                        <h2 id="speed">3. Understanding Speed the Right Way</h2>
                        <h3>Mbps vs MBps</h3>
                        <p>Plans are sold in <strong>Mbps</strong> (megabits). File sizes are in <strong>MB</strong> (megabytes). 8 bits = 1 byte, so a 100 Mbps line downloads at about 12.5 MB per second. Providers who quote "MB" to look bigger are being misleading. More on units in our <a href="/blog/mbps-guide-bilimora">Mbps guide</a>.</p>

                        <h3>Symmetrical vs asymmetrical</h3>
                        <p>Most home plans are <strong>asymmetrical</strong> — fast download, slower upload (e.g. 100 down / 50 up). That's fine for browsing and streaming. If you upload a lot (YouTube creators, cloud backups, CCTV, video calls), ask about <strong>upload speed</strong> specifically, or a <strong>symmetrical</strong> plan (equal up and down). See <a href="/blog/internet-for-youtube-creators-bilimora">internet for creators</a>.</p>

                        <h3>FUP (the hidden cap)</h3>
                        <p><strong>FUP (Fair Usage Policy)</strong> is a data cap that throttles your speed after a limit. Always ask whether a plan is truly unlimited or has an FUP. Nisan plans are genuinely unlimited — details in <a href="/blog/unlimited-internet-plans-bilimora">unlimited internet plans</a>.</p>

                        <h2 id="ip">4. Your IP Address — What to Know</h2>
                        <p>An <strong>IP address</strong> is your connection's address on the internet. There are a few types worth understanding before you buy:</p>

                        <h3>Dynamic vs static IP</h3>
                        <ul>
                            <li><strong>Dynamic IP</strong> — changes from time to time. This is the default for homes and works for everything: browsing, streaming, gaming, video calls.</li>
                            <li><strong>Static IP</strong> — a fixed, unchanging address. Needed only if you host a server, want remote access to your home CCTV/NAS, run a VPN into your home, or have work that requires a fixed address. It's usually a paid add-on.</li>
                        </ul>

                        <h3>Public vs private &amp; CGNAT</h3>
                        <p>Your <strong>public IP</strong> is what the internet sees; your <strong>private IPs</strong> (like 192.168.x.x) are your devices at home. Some ISPs put many users behind one public IP using <strong>CGNAT</strong> (Carrier-Grade NAT) to save addresses. CGNAT is fine for normal use but can block remote CCTV access, port forwarding and some gaming. If you need remote access, ask: <em>"Do you use CGNAT, and can I get a real public or static IP?"</em></p>

                        <h3>IPv4 vs IPv6</h3>
                        <p><strong>IPv4</strong> is the older address format (running out globally); <strong>IPv6</strong> is the newer, near-unlimited one. Good ISPs support both. For most users it makes no visible difference — but IPv6 support is a sign of a modern network.</p>

                        <div class="cost-box"><strong>How to find your IP address:</strong> Search <strong>"what is my IP"</strong> on Google to see your public IP instantly. For a device's local IP: on a phone, open WiFi settings and tap the network; on Windows run <code>ipconfig</code>; on Mac/Linux run <code>ifconfig</code> or <code>ip a</code>. Your router's admin page shows both public and local IPs.</div>

                        <h2 id="metrics">5. Latency, Jitter, Packet Loss &amp; Uptime</h2>
                        <p>Speed isn't everything. For calls, gaming and video these matter just as much:</p>
                        <ul>
                            <li><strong>Latency (ping):</strong> delay in milliseconds. Lower is better. Fiber gives ~5–20 ms; good for gaming and calls. High ping causes lag even on "fast" connections.</li>
                            <li><strong>Jitter:</strong> variation in latency. High jitter makes video calls stutter. Fiber keeps jitter low.</li>
                            <li><strong>Packet loss:</strong> data that never arrives, causing freezes and drops. Should be near zero.</li>
                            <li><strong>Uptime / SLA:</strong> how often the connection is actually working. Ask about typical uptime and how fast faults are fixed.</li>
                        </ul>
                        <p>You can check your own numbers any time with our <a href="/speedtest.php">free speed test</a> — note the download, upload and ping figures.</p>

                        <h2 id="checklist">6. The Pre-Purchase Checklist — What to Ask</h2>
                        <p>Before you pay any ISP, run through these questions. A good provider answers all of them clearly:</p>
                        <ol>
                            <li><strong>Technology:</strong> Is it FTTH fiber, cable, DSL or wireless at my exact address?</li>
                            <li><strong>Real speed:</strong> What download <em>and</em> upload speed will I actually get?</li>
                            <li><strong>Bandwidth type:</strong> Is it shared or dedicated? What's the contention ratio?</li>
                            <li><strong>Data cap:</strong> Is it truly unlimited, or is there an FUP limit?</li>
                            <li><strong>Latency:</strong> What's typical ping? (important for gaming &amp; calls)</li>
                            <li><strong>Uptime &amp; support:</strong> What's the uptime, and how fast are faults fixed? Local team or call centre?</li>
                            <li><strong>IP address:</strong> Dynamic or static? Do you use CGNAT? Is a static IP available if I need it?</li>
                            <li><strong>Total price:</strong> Is GST included? Any hidden setup or activation fees?</li>
                            <li><strong>Installation &amp; router:</strong> Are installation and the WiFi router free?</li>
                            <li><strong>Contract &amp; refund:</strong> Any lock-in? Is there a money-back guarantee if I'm not satisfied?</li>
                            <li><strong>Coverage:</strong> How many customers do you already serve in my area?</li>
                            <li><strong>Support hours:</strong> Are you available on Sundays and holidays?</li>
                        </ol>

