                    <li><strong>Parent-teacher communication:</strong> WhatsApp video calls with teachers, parent meetings via Zoom</li>
                    <li><strong>Educational apps:</strong> Duolingo, reading apps, science simulations — all need internet</li>
                </ul>

                <h2>Speed Required for School Activities</h2>

                <ul>
                    <li>One online class (Zoom/Google Meet): 5–10 Mbps</li>
                    <li>One video lecture playback (YouTube, BYJU's): 3–8 Mbps</li>
                    <li>Online test submission: 1–2 Mbps (but needs to be stable)</li>
                    <li>PDF and document downloads: 1–5 Mbps (faster = less waiting)</li>
                    <li>Parent on a video call simultaneously: 5–10 Mbps</li>
                </ul>

