                <h3>3. Check your router placement</h3>
                <p>If your child's study room is far from the router, WiFi signal through concrete walls may reduce effective speed. Ask Nisan to run a cable to the study room during installation — your child gets full connection speed for their class, not a reduced WiFi signal.</p>

